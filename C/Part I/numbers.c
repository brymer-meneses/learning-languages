/* A Program that uses Characters, Integers, 
    and Floating-Point Data Types */

#include <stdio.h>
int main() {
    printf("I am learning the %c programming language\n", 'C');
    printf("I have just completed Chapter %d\n", 2);
    printf("I am %.1f percent ready to move on ", 99.9);
    return 0;
}

/* A character (c), */
/* An integer (2) */
/* A floating-point number (99.9) */
